def mm2ft(mm):   return mm * .003281
def ft2mm(ft):   return ft / .003281
def mm2in(mm):   return mm * .03937
def in2mm(inch): return inch / .03937
def mm2m(mm):    return mm * .001
def m2mm(m):     return m  / .001

